# Example of using if-else ladder in Python
z = int(input())
# a = 100
# b = 200
# c = 300
# d = 400
# e = 500
if z == 100:
    print("a is 100")
elif z == 200:
    print("b is 200")
elif z == 300:
    print("c is 300")
elif z == 400:
    print("d is 400")
elif z == 500:
    print("e is 500")
else:
    print("wrong input")

 